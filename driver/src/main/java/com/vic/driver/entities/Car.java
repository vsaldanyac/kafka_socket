package com.vic.driver.entities;


public class Car {

	private int    code;
	private String brand;
	private String model;
	private String plate;
	private String color;
	private String ambientalDistinctive;

	public int getCode() {
		return code;
	}
	public void setCode(int code) {
		this.code = code;
	}
	public String getBrand() {
		return brand;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}
	public String getModel() {
		return model;
	}
	public void setModel(String model) {
		this.model = model;
	}
	public String getPlate() {
		return plate;
	}
	public void setPlate(String plate) {
		this.plate = plate;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public String getAmbientalDistinctive() {
		return ambientalDistinctive;
	}
	public void setAmbientalDistinctive(String ambientalDistinctive) {
		this.ambientalDistinctive = ambientalDistinctive;
	}
}
